#include <iostream>

using namespace std;


int main() {
    // Insertion Operation
    int x = 3;

    cout << "Hi!\n";
    cout << 3.14 << endl;
    cout << x << endl;
    cout << "Enter your name: ";

    // Extration operator
    string name;
    cin >> name;

    cout << name << endl;

    return 0;
}